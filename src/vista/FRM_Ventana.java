    public void cambiarIcono()
    {
        contador++;
        if(contador%2==0)
        {
            jl_Tiempo.setIcon(iconoUno);
        }
        else
        {
            jl_Tiempo.setIcon(iconoDos);    
        }
        
    }
    
    public void moverFondo()
    {
        if(jl_Fondo.getX()>-350)
        {
            jl_Fondo.setLocation(jl_Fondo.getX()-10, jl_Fondo.getY());
        }
        else
        {
            jl_Fondo.setLocation(0, jl_Fondo.getY());
        }
    }
    
    public void moverObstaculo()
    {
        if(jl_Obstaculo.getX()>-50)
        {
            jl_Obstaculo.setLocation(jl_Obstaculo.getX()-15, jl_Obstaculo.getY());
        }
        else
        {
            jl_Obstaculo.setLocation(400, jl_Obstaculo.getY());
        }
    }
    
